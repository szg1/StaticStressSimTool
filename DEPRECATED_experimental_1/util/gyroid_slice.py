"""
gyroid_slicer.py

Purpose: mathematically slice a triangle mesh into layers and produce a gyroid
infill as analytic implicit-level-contours. Supports nozzle width, layer
height, wall count (including top and bottom layers).

Design notes:
 - Uses exact triangle-plane intersection to build 2D polygons for each layer.
 - Defines gyroid as an analytic implicit function G(x,y,z)-t = 0 and traces
   contours in the layer plane by predictor-corrector continuation.
 - Walls (perimeter) are generated by polygon offset using pyclipper if
   available. If pyclipper is not installed, the code will raise a helpful
   error telling you to pip install pyclipper (that's the best option).
 - Top/bottom layers are handled by producing a simple parallel-line fill
   (mathematically clipped to layer polygon) to ensure a solid surface.

Dependencies (recommended): numpy, pyclipper, shapely (optional but useful
for plotting / geometry checks). All are pip-installable.

This is a practical, readable implementation rather than the absolutely
most-optimized production slicer. It focuses on numerical robustness and
clarity.

Example usage at bottom.
"""

